package day18_NestedLoop;

public class NestedLoopPractice2 {

    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {

            for (int i = 0; i < 10; i++) {
                System.out.print("* ");
            }
            System.out.print("* ");
        }


        System.out.println("--------------------");


        for (int i = 1; i <= 5 ; i++) {


            for (int j = 1; j <=i ; j++) {

                System.out.print("* ");
            }

            System.out.println();


        }













    }


}



























































